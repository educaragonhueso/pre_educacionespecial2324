<h2><b>B.- Sobre la solicitud:<b></h2>
<!--PREGUNTA 10-->
   <div class="panel panel-default ">
      <div class="panel-heading accordion-toggle question-toggle" data-toggle="collapse" data-parent="#faqAccordion" data-target="#question10" aria-expanded="true" >
         <h4 class="panel-title titulofaq"></h4>
         <h3> 
            ¿Cómo se escolarizan los hermanos que participan en el proceso?
         </h3>
      </div>
      <div id="question10" class="panel-collapse collapse" style="">
         <div class="panel-body" <?php echo $editable; ?>>
         <p>
            La escolarización de hermanos puede realizarse de forma individualizada o de forma conjunta.<br> Cuando los padres/tutores opten por la escolarización conjunta de hermanos cumplimentarán la "Solicitud conjunta de hermanos", con una única relación priorizada de centros escolares.<br> Con esta solicitud está expresando el deseo de obtener plaza para todos los hermanos en un mismo centro, como criterio preferente. Esto implica:<br><br> a. Pérdida de la reserva de plaza en los casos en que todos o alguno de los hermanos provengan de centros docentes sostenidos con fondos públicos de la Comunidad Autónoma de Aragón, salvo en el caso de que se realice una solicitud conjunta y todos los hermanos estén escolarizados en el mismo centro de origen y soliciten la reserva de plaza.<br> b. Los padres/tutores renuncian a los derechos individuales de cada uno de sus hijos, por una escolarización de todos ellos en un mismo centro. Ello supone que hasta que no haya plazas para todos los hermanos en el mismo centro, se considerará que no existen plazas escolares para ellos.<br> c. En los centros en los que existan solicitudes de escolarización conjunta de hermanos, se comenzará a baremar por el nivel correspondiente a la menor edad.<br> d. Cuando uno de los hermanos, tras la aplicación de los criterios de admisión y desempate, pudiera obtener plaza en el centro indicado en primera opción, se le otorgarán al resto de hermanos 8 puntos por hermano matriculado en el centro.<br><br>Cuando los padres/tutores opten por una escolarización individual para cada uno de los hermanos, éstos podrán participar por separado en diferentes centros, sin que resulte de aplicación lo especificado para la escolarización conjunta.<br> En el caso de hermanos de la misma edad (nacidos en el mismo año y solicitan el mismo curso) la obtención de plaza por alguno de ellos, supondrá la escolarización de estos hermanos en el mismo centro, teniendo preferencia sobre los inmediatos anteriores que se encuentren empatados a puntos, en caso de superar el límite máximo de alumnos por aula.

        </p>
      </div>
  </div>
